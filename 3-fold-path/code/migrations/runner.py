def schema_migrations_exists(conn, engine_type: str) -> bool:
    """Check whether schema_migrations table exists (engine-portable)."""
    if engine_type == "sqlite":
        result = conn.execute(
            text("SELECT name FROM sqlite_master WHERE type='table' AND name='schema_migrations'")
        ).fetchone()
        return result is not None
    elif engine_type == "mysql":
        result = conn.execute(
            text("SHOW TABLES LIKE 'schema_migrations'")
        ).fetchone()
        return result is not None
    else:  # postgres
        result = conn.execute(
            text(
                "SELECT to_regclass('public.schema_migrations')"
            )
        ).fetchone()
        return result is not None and result[0] is not None


def get_recorded_hash(conn, version: str) -> Optional[str]:
    """Return the sha256_hex recorded for this version, or None if absent."""
    row = conn.execute(
        text("SELECT sha256_hex FROM schema_migrations WHERE version = :v"),
        {"v": version},
    ).fetchone()
    return row[0] if row else None


def record_migration(conn, version: str, description: str, sha256: str,
                     duration_ms: int, engine_type: str) -> None:
    """Insert a row into schema_migrations (ARCH-SPEC §8.4).

    OQ-029 (D-09): applied_at must be a datetime object, not an ISO-8601 string.
    MySQL DATETIME rejects the 'Z' suffix; SQLAlchemy renders datetime objects
    correctly for all three engines (sqlite / mysql / postgres).
    """
    now_utc = datetime.now(timezone.utc).replace(tzinfo=None)  # naive UTC — portable across all engines
    conn.execute(
        text(
            "INSERT INTO schema_migrations "
            "(version, description, applied_at, applied_by, sha256_hex, duration_ms) "
            "VALUES (:version, :description, :applied_at, :applied_by, :sha256_hex, :duration_ms)"
        ),
        {
            "version": version,
            "description": description,
            "applied_at": now_utc,
            "applied_by": f"runner.py/{engine_type}",
            "sha256_hex": sha256,
            "duration_ms": duration_ms,
        },
    )


def apply_migration_file(path: Path, conn, engine_type: str, raw_sql: str) -> str:
    """
    Apply a single migration file. Returns one of: 'applied', 'skipped', 'aborted'.
    Raises RuntimeError on drift detection.
    """
    version = parse_version(path.name)
    description = parse_description(path.name)

    up_raw = extract_up_section(raw_sql)
    up_substituted = apply_tokens(up_raw, engine_type)
    sha256 = sha256_of(up_raw)  # Hash computed on pre-substitution text (canonical form)

    # Only query schema_migrations if the table already exists (bootstrap guard).
    # On first run, file 000 creates the table; we must not query it before it exists.
    if schema_migrations_exists(conn, engine_type):
        recorded_hash = get_recorded_hash(conn, version)
        if recorded_hash is not None:
            if recorded_hash == sha256:
                print(f"  [{version}] SKIP  — already applied, hash matches.")
                return "skipped"
            else:
                msg = (
                    f"DRIFT DETECTED for version {version}: "
                    f"recorded sha256={recorded_hash[:12]}… "
                    f"file sha256={sha256[:12]}… "
                    f"— refusing apply (ARCH-SPEC §8.4)."
                )
                raise RuntimeError(msg)

    # Apply — strip comments before splitting to avoid semicolons inside -- lines
    t0 = time.monotonic()
    statements = split_statements(up_substituted)
    for stmt in statements:
        conn.execute(text(stmt))
    duration_ms = int((time.monotonic() - t0) * 1000)

    record_migration(conn, version, description, sha256, duration_ms, engine_type)
    print(f"  [{version}] APPLIED — {description} ({duration_ms}ms, sha256={sha256[:12]}…)")
    return "applied"


def reset_pg_identity_sequences(conn) -> None:
    """OQ-031 (D-09): Reset PostgreSQL IDENTITY sequences after seed inserts.

    GENERATED BY DEFAULT AS IDENTITY sequences are NOT advanced when rows are
    inserted with explicit PK values (as seed migrations do). Without this fix
    the next API INSERT without an explicit id collides with seed row id=1.

    Queries information_schema to discover all 'id' identity columns in the
    public schema, then calls setval(pg_get_serial_sequence(...)) for each.
    Safe to call on an empty schema (COALESCE guards against NULL from MAX).
    """
    id_tables = conn.execute(text(
        "SELECT table_name FROM information_schema.columns "
        "WHERE column_name = 'id' AND table_schema = 'public' "
        "  AND identity_generation IS NOT NULL "
        "ORDER BY table_name"
    )).fetchall()

    reset_count = 0
    for (table_name,) in id_tables:
        conn.execute(text(
            f"SELECT setval(pg_get_serial_sequence('{table_name}', 'id'), "
            f"  COALESCE((SELECT MAX(id) FROM {table_name}), 1))"
        ))
        reset_count += 1

    if reset_count:
        print(f"  [OQ-031] Reset {reset_count} PostgreSQL identity sequence(s).")


def run_migrations(engine_type: str, db_url: str) -> None:
    """Entry point: apply all pending migrations in order."""
    print(f"\nMI-M-T migration runner v0.1.0")
    print(f"Engine : {engine_type}")
    print(f"DB URL : {db_url}\n")

    files = collect_migration_files()
    if not files:
        print("No migration files found in", MIGRATIONS_DIR)
        return

    # SQLite: ensure parent directory exists
    if engine_type == "sqlite":
        db_path = db_url.replace("sqlite:////", "/").replace("sqlite:///", "")
        os.makedirs(os.path.dirname(db_path), exist_ok=True)

    engine = create_engine(db_url, echo=False)

    with engine.begin() as conn:
        counts = {"applied": 0, "skipped": 0, "aborted": 0}

        for path in files:
            raw_sql = path.read_text(encoding="utf-8")
            try:
                result = apply_migration_file(path, conn, engine_type, raw_sql)
                counts[result] += 1
            except RuntimeError as exc:
                print(f"  ABORT: {exc}")
                counts["aborted"] += 1
                sys.exit(1)

    # OQ-031 (D-09): PostgreSQL IDENTITY sequences don't advance on explicit-ID
    # seed inserts (GENERATED BY DEFAULT AS IDENTITY). MySQL AUTO_INCREMENT
    # self-heals; SQLite has no sequences. Reset all identity sequences after
    # migrations complete so that subsequent API inserts don't collide.
    if engine_type == "postgres":
        with engine.begin() as conn:
            reset_pg_identity_sequences(conn)

    print(f"\nDone. applied={counts['applied']} skipped={counts['skipped']} aborted={counts['aborted']}")

    # Print final state for SESSION-NOTES
    engine2 = create_engine(db_url, echo=False)
    with engine2.connect() as conn:
        rows = conn.execute(
            text("SELECT version, description, applied_at, applied_by, sha256_hex, duration_ms "
                 "FROM schema_migrations ORDER BY version")
        ).fetchall()
        print("\nschema_migrations contents:")
        print(f"  {'version':<8} {'applied_at':<22} {'sha256_hex[:16]':<18} {'description'}")
        print(f"  {'-'*8} {'-'*22} {'-'*18} {'-'*40}")
        for row in rows:
            print(f"  {row[0]:<8} {str(row[2]):<22} {row[4][:16]+'…':<18} {row[1]}")


# ---------------------------------------------------------------------------
# CLI entry point
# ---------------------------------------------------------------------------

def main() -> None:
    parser = argparse.ArgumentParser(
        description="MI-M-T migration runner — applies NNN_*.sql files in order."
    )
    parser.add_argument(
        "--engine",
        choices=["sqlite", "mysql", "postgres"],
        default="sqlite",
        help="Target engine (default: sqlite)",
    )
    parser.add_argument(
        "--db-url",
        default=None,
        help="SQLAlchemy connection URL (overrides default for the chosen engine)",
    )
    args = parser.parse_args()

    db_url = args.db_url or DEFAULT_URLS[args.engine]
    run_migrations(args.engine, db_url)


if __name__ == "__main__":
    main()



