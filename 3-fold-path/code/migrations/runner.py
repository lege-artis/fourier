def apply_pending(engine_type: str = "sqlite", db_url: Optional[str] = None) -> None:
    """Callable entry point for Alembic env.py.

    Usage in env.py::

        from migrations.runner import apply_pending
        apply_pending(engine_type=config.get_main_option("engine"),
                      db_url=config.get_main_option("sqlalchemy.url"))
    """
    url = db_url or DEFAULT_URLS[engine_type]
    run_migrations(engine_type, url)
