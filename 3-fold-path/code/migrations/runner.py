def apply_tokens(sql: str, engine: str) -> str:
    """Substitute canonical tokens for the target engine (ARCH-SPEC §8.3)."""
    substitutions = TOKENS[engine]
    for token, replacement in substitutions.items():
        sql = sql.replace(token, replacement)
    return sql


def split_statements(sql: str) -> list[str]:
    """
    Split SQL into executable statements using a character-level state machine.

    Handles three cases that defeat naive split(';'):
      1. Semicolons inside -- single-line comments  (e.g. comment text with ';')
      2. Semicolons inside \'...' string literals    (e.g. sync_error values)
      3. Escaped single-quotes inside strings       (e.g. O\'Brien)

    Algorithm:
      - Track in_string / in_comment state character by character.
      - Only treat ';' as a statement delimiter when in the neutral state.
      - Collect accumulated characters into statement chunks.
    """
    statements: list[str] = []
    current: list[str] = []
    in_string = False
    in_comment = False
    i = 0
    n = len(sql)

    while i < n:
        c = sql[i]

        if in_comment:
            if c == "\n":
                in_comment = False          # comment ends at newline
            i += 1
            continue

        if in_string:
            current.append(c)
            if c == "\'":
                # SQL escape: two consecutive single-quotes inside a string
                if i + 1 < n and sql[i + 1] == "\'":
                    current.append("\'")
                    i += 2
                    continue
                else:
                    in_string = False       # closing quote
            i += 1
            continue

        # Neutral state
        if c == "-" and i + 1 < n and sql[i + 1] == "-":
            in_comment = True
            i += 2
            continue

        if c == "\'":
            in_string = True
            current.append(c)
            i += 1
            continue

        if c == ";":
            stmt = "".join(current).strip()
            if stmt:
                statements.append(stmt)
            current = []
            i += 1
            continue

        current.append(c)
        i += 1

    # Trailing content after last semicolon
    stmt = "".join(current).strip()
    if stmt:
        statements.append(stmt)

    return statements


def extract_up_section(sql: str) -> str:
    """
    Return everything above the DOWN BEGIN fence.
    If no DOWN section exists, return the whole file.
    """
    match = re.search(r"--\s*DOWN BEGIN", sql, re.IGNORECASE)
    if match:
        return sql[: match.start()]
    return sql


def sha256_of(text_content: str) -> str:
    """SHA-256 of normalised (stripped) UP section — ARCH-SPEC §8.4."""
    normalised = text_content.strip()
    return hashlib.sha256(normalised.encode("utf-8")).hexdigest()


def collect_migration_files() -> list[Path]:
    """Return NNN_*.sql files in MIGRATIONS_DIR, sorted by NNN prefix."""
    files = sorted(
        MIGRATIONS_DIR.glob("[0-9][0-9][0-9]_*.sql"),
        key=lambda p: p.name,
    )
    return files


def parse_version(filename: str) -> str:
    """Extract the NNN prefix from a filename like '000_create_schema_migrations.sql'."""
    return filename.split("_")[0]


def parse_description(filename: str) -> str:
    """Extract human description from filename (strip NNN_ prefix and .sql suffix)."""
    stem = Path(filename).stem          # e.g. "000_create_schema_migrations"
    return stem[4:].replace("_", " ")  # e.g. "create schema migrations"


